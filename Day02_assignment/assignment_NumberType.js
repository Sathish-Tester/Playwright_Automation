function numberType (num1)
{   
    if (num1>0)
    {        console.log(num1 + " is a positive number.");
    }
    else if (num1<0)
    {        console.log(num1 + " is a negative number.");
    }
    else    {
        console.log(num1 + " is zero.");
    }

}

numberType(10);
numberType(-5);
numberType(0);



